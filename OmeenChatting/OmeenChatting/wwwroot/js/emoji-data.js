window.getRecentEmojis = function() {
    const recent = localStorage.getItem('recentEmojis');
    return recent ? JSON.parse(recent) : ['😊', '❤️', '😂', '👍', '😍', '🔥', '🎉', '💯'];
};

window.addRecentEmoji = function(emoji) {
    let recent = window.getRecentEmojis();
    
    // Remove emoji if it already exists
    recent = recent.filter(e => e !== emoji);
    
    // Add to beginning
    recent.unshift(emoji);
    
    // Keep only the first 20
    recent = recent.slice(0, 20);
    
    localStorage.setItem('recentEmojis', JSON.stringify(recent));
    window.emojiData.recent = recent;
};

// Initialize recent emojis
window.emojiData.recent = window.getRecentEmojis();